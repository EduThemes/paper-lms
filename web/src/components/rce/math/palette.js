/**
 * Math equation editor symbol palette.
 *
 * Mirrors Canvas LMS's palette inventory (canvas-rce/.../EquationEditorToolbar/buttons.js)
 * so that teachers migrating from Canvas see the same symbols and templates.
 *
 * Each entry has:
 *   command          — LaTeX inserted in WYSIWYG mode (with \placeholder{} for templates).
 *   advancedCommand  — LaTeX inserted in raw-LaTeX mode (no \placeholder, plain braces).
 *   displayName      — small LaTeX rendered as the button label (preferred when set).
 *   label            — human-readable description, used as aria-label and tooltip.
 */

const PLACEHOLDER = '\\placeholder{}';

/** Convenience helper for entries that are simple symbols (button shows the rendered LaTeX). */
const sym = (command, label) => ({ command, label });

/** Convenience helper for templates with a fillable display + raw fallback. */
const tmpl = (displayName, withPlaceholder, withoutPlaceholder, label) => ({
  command: withPlaceholder,
  advancedCommand: withoutPlaceholder,
  displayName,
  label,
});

