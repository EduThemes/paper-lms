/**
 * Patterns Canvas's WYSIWYG (mathlive) cannot render — when present in existing
 * LaTeX, force the modal into Advanced (raw textarea) mode so the user can edit
 * the source. Mirrors canvas-rce/.../advancedOnlySyntax.js.
 */
export const ADVANCED_ONLY_PATTERNS = [
  /\\text\b/,
  /\\mbox\b/,
  /\\newcommand\b/,
  /\\def\b/,
  /\\bbox\b/,
  /\\newline\b/,
  /\\\\/,
];

export const requiresAdvancedMode = (latex) =>
  !!latex && ADVANCED_ONLY_PATTERNS.some((re) => re.test(latex));
